package Objects;

/**
 *
 * @author luisj
 */
public class Habilidad {

    private Integer id;
    private String nivel;
    private String nombre;

    public Habilidad() {
    }

    public Habilidad(Integer id, String nivel, String nombre) {
        this.id = id;
        this.nivel = nivel;
        this.nombre = nombre;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getNivel() {
        return nivel;
    }

    public void setNivel(String nivel) {
        this.nivel = nivel;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    @Override
    public String toString() {
        return this.nombre;
    }
}
